@dataclass
class InstrumentSignature:
    """The harmonic fingerprint of one market instrument."""
    symbol: str
    dominant_frequency_hz: float = 0.0      # Primary cycle frequency
    secondary_frequencies: List[float] = field(default_factory=list)
    phase_offset: float = 0.0               # Current phase in cycle
    amplitude: float = 0.0                  # Volatility = amplitude
    energy_state: str = "neutral"           # "accumulating" / "distributing" / "neutral"
    energy_flow_mw: float = 0.0             # Positive = inflow, negative = outflow
    bot_activity: float = 0.0               # 0-1 mechanical pattern strength
    whale_presence: float = 0.0             # 0-1 large player activity
    phi_alignment: float = 0.0             # How close to PHI-ratio cycles
    schumann_coupling: float = 0.0          # How aligned with Earth frequency
    extraction_pattern: str = ""            # Which of the 9 patterns active


@dataclass
class MarketEnergyState:
    """The entire market as one energy field."""
    timestamp: float = 0.0
    total_energy: float = 0.0               # Sum of all instrument energies
    net_flow: float = 0.0                   # Net energy direction
    coherence: float = 0.0                  # How synchronized are instruments
    dominant_frequency: float = 0.0          # Market-wide dominant cycle
    fear_greed: int = 50
    instruments: Dict[str, InstrumentSignature] = field(default_factory=dict)
    extraction_active: bool = False          # Are bots extracting?
    accumulation_active: bool = False        # Are whales accumulating?


